# Day 1 - 30DaysOfPython Challenge

print(2 + 3)             # addition(+)
print(3 - 1)             # subtraction(-)
print(2 * 3)             # multiplication(*)
print(3 / 2)             # division(/)
print(3 ** 2)            # exponential(**)
print(3 % 2)             # modulus(%)
print(3 // 2)            # Floor division operator(//)

# Checking data types
print(type(15))          # Int
print(type(3.414))        # Float
print(type(1 - 3j))      # Complex number
print(type('Gerard'))  # String
print(type([1, 4, 3]))   # List
print(type({'name':'Asabeneh'})) # Dictionary
print(type({9.8, 12, 2.7}))    # Set
print(type((9.8, 3.14, 2.7)))    # Tuple

# Finding the euclidian distance between two points (2,3) and (10,8)
distance = ((10-2)**2 + (8-3)**2)**0.5
print(distance)
